const seats = input.split('\n').map(row => row.split(''));

const EMPTY_SEAT = 'L';
const OCCUPIED_SEAT = '#';
const FLOOR = '.';

const countOccupiedSeats = currentSears => currentSears.flatMap(m => m).filter(m => m === OCCUPIED_SEAT).length;
const getPosition = (currentSeat, x, y) => x === false || y === false ? false : currentSeat[y][x];
const getAdjacentSeats = (currentSeat, x, y) => {
  const xplus = x === (currentSeat[0].length - 1) ? false : x + 1;
  const xminus = (x === 0) ? false : x - 1;

  const yplus = y === (currentSeat.length - 1) ? false : y + 1;
  const yminus = (y === 0) ? false : y - 1;

  const adjacentSeats = [
    getPosition(currentSeat, x, yminus),
    getPosition(currentSeat, xplus, yminus),
    getPosition(currentSeat, xplus, y),
    getPosition(currentSeat, xplus, yplus),
    getPosition(currentSeat, x, yplus),
    getPosition(currentSeat, xminus, yminus),
    getPosition(currentSeat, xminus, y),
    getPosition(currentSeat, xminus, yplus),
  ];

  return adjacentSeats;
};

const simulatePlacement = (currentSeats) => {
  const newSeats = [];

  for (let x = 0;x < currentSeats[0].length; x++) {
    const newRow = [];
    for (let y = 0;y < currentSeats.length; y++) {
      const currentSeat = currentSeats[y][x];
      const adjacentSeats = getAdjacentSeats(currentSeats, x, y);

      if (currentSeat === EMPTY_SEAT && !adjacentSeats.includes(OCCUPIED_SEAT)) {
        newRow.push(OCCUPIED_SEAT);
      } else if (currentSeat === OCCUPIED_SEAT && adjacentSeats.filter(seat => seat === OCCUPIED_SEAT).length >= 4) {
        newRow.push(EMPTY_SEAT);
      } else {
        newRow.push(currentSeat);
      }
    }

    newSeats.push(newRow);
  }

  return newSeats;
};

