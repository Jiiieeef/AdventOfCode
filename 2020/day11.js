const getSeatInDirection = (currentSeats, x, y, newX, newY) => {
  let seat, meetBorder;

  while (!meetBorder && !seat) {
    if (x + newX === currentSeats[0].length || x + newX < 0 || y + newY === currentSeats.length || y + newY < 0) {
      meetBorder = true;
    } else {
      x += newX;
      y += newY;

      if (currentSeats[y][x] !== FLOOR) {
        seat = currentSeats[y][x];
      }
    }
  }
  return seat;
};

const getVisibleSeats = (currentSeat, x, y) => {
  const adjacentSeats = [
    getSeatInDirection(currentSeat, x, y, 1, -1),
    getSeatInDirection(currentSeat, x, y, 1, 0),
    getSeatInDirection(currentSeat, x, y, 1, 1),
    getSeatInDirection(currentSeat, x, y, 0, -1),
    getSeatInDirection(currentSeat, x, y, 0, 1),
    getSeatInDirection(currentSeat, x, y, -1, -1),
    getSeatInDirection(currentSeat, x, y, -1, 0),
    getSeatInDirection(currentSeat, x, y, -1, 1),

  ];

  return adjacentSeats;
};

